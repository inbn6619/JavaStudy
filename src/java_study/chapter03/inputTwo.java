package java_study.chapter03;

public class inputTwo {

	public static void main(String[] args) {
		int n1 = 10;
		int n2 = 4;

		System.out.println(n1 + n2);
		System.out.println(n1 - n2);
		System.out.println(n1 / n2);
		System.out.println(n1 * n2);
		System.out.println(10.0 / 4);

		System.out.println(n1 / (double) n2);
	}
}
